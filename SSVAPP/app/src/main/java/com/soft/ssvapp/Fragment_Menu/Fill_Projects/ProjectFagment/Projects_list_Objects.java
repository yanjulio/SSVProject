package com.soft.ssvapp.Fragment_Menu.Fill_Projects.ProjectFagment;

public class Projects_list_Objects {

    private String title, details, bilan;

    public Projects_list_Objects(String title, String details, String bilan) {
        this.title = title;
        this.details = details;
        this.bilan = bilan;
    }

    public String getTitle() {
        return title;
    }

    public String getDetails() {
        return details;
    }

    public String getBilan() {
        return bilan;
    }
}
